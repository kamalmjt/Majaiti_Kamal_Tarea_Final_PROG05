
public class BooleanConMensaje {

	private boolean error;
	private String mensajeError;
	
	
	public BooleanConMensaje(boolean error, String mensajeError) {
		this.error = error;
		this.mensajeError = mensajeError;
	}
	
	public boolean isError() {
		return error;
	}
	public String getMensaje() {
		return mensajeError;
	}
	
	
	
	
	
	
	
	
}
